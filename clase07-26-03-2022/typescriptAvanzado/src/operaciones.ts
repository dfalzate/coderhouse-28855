export function suma(a: number, b: number): number {
  return a + b;
}
export const resta = (a: number, b: number): number => {
  return a - b;
};
export function multiplicacion(a: number, b: number): number {
  return a * b;
}
export const division = (a: number, b: number): number => {
  return a / b;
};
